#include <iostream>
#include <algorithm>
using namespace std;

int main()
{
    cout << " El minimo de 12 y 7 es: " << min(12, 7);
    cout << "\n El maximo de 12 y 7 es: " << max(12, 7);
    cout << "\n El minimo de 'G' y 'Z' es: " << min('G', 'Z');
    cout << "\n El maximo de 'G' y 'Z' es: " << max('G', 'Z');
    cout << endl;
    return 0;
}
